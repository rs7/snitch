-module(users).

%% API
-export([filterActive/1]).

%%====================================================================
%% API
%%====================================================================

filterActive(Users) ->
  Response = vk:users(Users, #{v => '5.52'}),
  [maps:get(<<"id">>, User) || User <- Response, not maps:is_key(<<"deactivated">>, User)].

